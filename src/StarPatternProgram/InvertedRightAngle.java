package StarPatternProgram;

public class InvertedRightAngle {

	public static void main(String[] args) {
    // Inverted right angle triangle
		/**** 
		 *** 
		 **
		 */
	for (int i=0 ; i <=5 ; i++)
	{
		for (int j=5 ; j>=i ; j--)
		{
			System.out.print("*");
		}
	System.out.println();
	}	
		
	}
}
