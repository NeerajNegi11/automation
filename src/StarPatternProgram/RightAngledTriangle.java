package StarPatternProgram;

public class RightAngledTriangle {

	public static void main(String[] args) {
	
	// Right angled triangle	
	   /* 
	    ** 
	    **
	    ****/
		
	for (int i=0 ; i <= 5; i++)
	{
		for (int j=0 ; j <=i ; j++)
		{
			System.out.print("*");
		}
		System.out.println();
	}
	}
}
